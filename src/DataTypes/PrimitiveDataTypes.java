package DataTypes;

public class PrimitiveDataTypes {
    public static void main(String[] args) {

        // Primitive Data Types: int, double, float, char, boolean, short.
        // Lesson Objective: Create a ninja - Naruto Uzumaki.

        int age = 16; // max value: <= 2147483647
        double height = 1.67;
        char initial = 'N';
        boolean liveOrDead = true;
        Long balance = 2147483648L; // > 2147483647

        System.out.println(age);
        System.out.println(height);
        System.out.println(initial);
        System.out.println(liveOrDead);
        System.out.println(balance);
    }
}
