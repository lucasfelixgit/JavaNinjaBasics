package DataTypes;

public class NonPrimitiveDataTypes {
    public static void main(String[] args) {

        // Non Primitive Data Types: String, Array, Class, Enum.
        // Lesson Objective: Create a ninja and apply methods on it.

        String name = "Naruto Uzumaki";
        String nameUpperCase = name.toUpperCase();

        String village = "Leaf Village";
        String villageLowerCase = village.toLowerCase();

        System.out.println("name = " + name);
        System.out.println("nameUpperCase = " + nameUpperCase);

        System.out.println("village = " + village);
        System.out.println("villageLowerCase = " + villageLowerCase);
    }
}
